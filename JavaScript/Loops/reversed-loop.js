const myFruits = ["apple", "orange", "pear"];


// nomarl for loop
for (let i = 0; i < myFruits.length; i++) {
  console.log(`The fruit index is: ${i} and fruit is: ${myFruits[i]}.`);
}



//reversed loop
for (let i = myFruits.length - 1; i >= 0; i--) {
  console.log(`The reversed loop fruit index is: ${i} and fruit is: ${myFruits[i]}.`);
}

